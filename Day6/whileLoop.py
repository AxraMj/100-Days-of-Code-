#while Loop
#while loop is used to iterate over a block of code as long as the test expression is true.
#difference between for loop and while loop:
#1. for loop is used when we know the number of iterations.
#2. while loop is used when we don't know the number of iterations in advance.

#syntax:
#while test_expression:
#    statement(s)

#example:
i = 1
while i <= 5:
    print(i)
    i += 1
print("Done")

#Note: if the test expression is false, the loop will be terminated and the control will come out of the loop.

#where for and while loop can be used:
#1. for loop: when the number of iterations is known.
#2. while loop: when the number of iterations is unknown.

